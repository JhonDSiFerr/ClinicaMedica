    private void btnSalvarProntuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarProntuarioActionPerformed
 // Se o prontuário ainda não existe, criamos um novo objeto
    if (this.prontuarioAtual == null) {
        this.prontuarioAtual = new Prontuario();
        // Precisamos associar este novo prontuário ao paciente correto
        Paciente p = new Paciente();
        p.setId(this.pacienteId);
        this.prontuarioAtual.setPaciente(p);
    }

    // Pega os textos da tela e atualiza o objeto
    this.prontuarioAtual.setReceituario(txaReceituario.getText());
    this.prontuarioAtual.setExames(txaExames.getText());
    this.prontuarioAtual.setObservacoes(txaObservacoes.getText());

    // Chama o método "salvar" inteligente do DAO
    ProntuarioDAO dao = new ProntuarioDAO();
    try {
        dao.salvar(this.prontuarioAtual);
        javax.swing.JOptionPane.showMessageDialog(this, "Prontuário salvo com sucesso!");
        this.dispose(); // Fecha a tela de prontuário após salvar
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar o prontuário:\n" + e.getMessage(), "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
// TODO add your handling code here:
    }//GEN-LAST:event_btnSalvarProntuarioActionPerformed

