  private void carregarProntuario() {
    ProntuarioDAO dao = new ProntuarioDAO();
    this.prontuarioAtual = dao.buscarPorPacienteId(this.pacienteId);

    // Se o paciente já tem um prontuário, preenchemos os campos
    if (this.prontuarioAtual != null) {
        txaReceituario.setText(prontuarioAtual.getReceituario());
        txaExames.setText(prontuarioAtual.getExames());
        txaObservacoes.setText(prontuarioAtual.getObservacoes());
    } 
 // Se for nulo, significa que é a primeira vez, então os campos ficam em branco.
}

