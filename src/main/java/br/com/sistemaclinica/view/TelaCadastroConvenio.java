    private void tblConveniosValueChanged(javax.swing.event.ListSelectionEvent evt) {                                             
    if (!evt.getValueIsAdjusting()) {
        int linhaSelecionada = tblConvenios.getSelectedRow();
        if (linhaSelecionada != -1) {
            // Pega o ID da coluna 0, converte para Integer e guarda na nossa variável
            idConvenioSelecionado = Integer.parseInt(tblConvenios.getValueAt(linhaSelecionada, 0).toString()); // <<-- ADICIONE ESTA LINHA

            // Pega os outros dados e preenche os campos
            String nome = tblConvenios.getValueAt(linhaSelecionada, 1).toString();
            String descricao = tblConvenios.getValueAt(linhaSelecionada, 2).toString();
            

            txtNomeConvenio.setText(nome);
            txaDescricaoConvenio.setText(descricao);
        
        }
    }
}
    
    private void carregarTabela() {
      ConvenioDAO dao = new ConvenioDAO();
    List<Convenio> lista = dao.listarTodos();
    DefaultTableModel modelo = (DefaultTableModel) tblConvenios.getModel();
    modelo.setRowCount(0);
    modelo.setColumnIdentifiers(new Object[]{"ID", "Nome", "Descrição"});
    for (Convenio c : lista) {
        modelo.addRow(new Object[]{c.getId(), c.getNome(), c.getDescricao()});
    }
    }
    
