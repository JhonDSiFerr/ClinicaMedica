    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
 // Coleta todos os dados da tela
    Convenio Convenio = new Convenio();
    Convenio.setNome(txtNomeConvenio.getText());
    Convenio.setDescricao(txaDescricaoConvenio.getText());

   
 

    // --- AQUI ESTÁ A LÓGICA ---
    try {
        ConvenioDAO dao = new ConvenioDAO();

        // Se idFuncionarioSelecionado é 0, então é um NOVO cadastro
        if (idConvenioSelecionado == 0) {
            dao.cadastrar(Convenio);
            javax.swing.JOptionPane.showMessageDialog(this, "Cônvenio cadastrado com sucesso!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } 
        // Senão, é uma ATUALIZAÇÃO
        else {
            // Define o ID no objeto para que o DAO saiba quem atualizar
            Convenio.setId(idConvenioSelecionado);
            dao.atualizar(Convenio);
            javax.swing.JOptionPane.showMessageDialog(this, "Cônvenio atualizado com sucesso!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }

        // Após salvar (seja criando ou atualizando), limpa o formulário e recarrega a tabela
        btnNovoActionPerformed(null); // Chama a ação do botão Novo para limpar tudo
        carregarTabela();

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar:\n" + e.getMessage(), "Erro no Banco de Dados", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
    
    // TODO add your handling code here:
    }//GEN-LAST:event_btnSalvarActionPerformed

