    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
   // 1. Verifica se algum funcionário foi selecionado na tabela.
        if (idConvenioSelecionado == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, selecione um Cônvenio na tabela para excluir.", "Nenhum funcionário selecionado", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 2. Pede confirmação ao usuário. Este é um passo MUITO importante para evitar exclusões acidentais.
        int resposta = javax.swing.JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este Cônvenio?\nEsta ação não pode ser desfeita.", "Confirmação de Exclusão", javax.swing.JOptionPane.YES_NO_OPTION);

        // 3. Se o usuário clicou em "Sim"...
        if (resposta == javax.swing.JOptionPane.YES_OPTION) {
            try {
                // Cria o DAO e chama o método deletar, passando o ID que já guardamos.
                ConvenioDAO dao = new ConvenioDAO();
                dao.deletar(idConvenioSelecionado);

                // Mostra a mensagem de sucesso.
                javax.swing.JOptionPane.showMessageDialog(this, "Cônvenio excluído com sucesso!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);

                // Limpa o formulário e recarrega a tabela para refletir a exclusão.
                btnNovoActionPerformed(null);
                carregarTabela();

            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Erro ao excluir Cônvenio:\n" + e.getMessage(), "Erro no Banco de Dados", javax.swing.JOptionPane.ERROR_MESSAGE);
            }
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeletarActionPerformed

